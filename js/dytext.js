DYText.prototype.copy = function () {

    var divides = this.divides;
    var ctx = this.ctx;

    // console.log(divides)

    ctx.globalCompositeOperation = "source-over";

    for (let index = 0; index < divides.length; index++) {
        var d = divides[index];

        var imgData = ctx.getImageData(d.get.x, d.get.y, d.get.width, d.get.height);

        ctx.putImageData(imgData, d.put.x, d.put.y);

        if (this.dev) {

            ctx.strokeStyle = "red";
            ctx.strokeRect(d.get.x, d.get.y, d.get.width, d.get.height);
        }
    };




};


DYText.prototype.toCanvas = function (targetCtx) {

    var ctx = this.ctx;
    targetCtx.canvas.width = 480;
    targetCtx.canvas.height = 280;

    var tw = ctx.canvas.width,
        th = ctx.canvas.height;

    targetCtx.fillStyle ='rgba(0,0,0,0.7)';
    this.bgImg ? targetCtx.drawImage(this.bgImg, 0, 0) : targetCtx.fillStyle =this.colorBg;
    targetCtx.fillRect(0, 0, 480, 280);
    targetCtx.fillStyle = 'rgba(255,255,255,.02)';

    var _i = Math.random() > 0.5 ? -1 : 1;
    var circle = {
        x: 22 + (Math.random() * 260 * _i),
        y: 125 + (Math.random() * 60 * _i),
        r: (Math.random() * 260)
    };

    targetCtx.arc(circle.x, circle.y, circle.r, 0, Math.PI * 2, true);
    var circle2 = {
        x: 225 + (Math.random() * 60 * _i),
        y: 240 + (Math.random() * 260 * _i),
        r: (Math.random() * 360)
    };

    targetCtx.arc(circle2.x, circle2.y, circle2.r, 0, Math.PI * 2, true);
    targetCtx.fill();


    var w = 480,
        h = w * th / tw,
        l = 240 - w / 2,
        t = 140 - h / 2;

    targetCtx.drawImage(ctx.canvas, l, t, w, h);
};



DYText.prototype.createFrames = function (num) {
    var frames = [],
        num = num || this.gifLength;

    for (let index = 0; index < num; index++) {
        this.update();

        var canvas1 = document.createElement('canvas'),
            ctx1 = canvas1.getContext('2d');

        //  canvas1.width = window.innerWidth;
        // canvas1.height = window.innerHeight;

        this.toCanvas(ctx1);

        frames.push(canvas1);

    };

    this.frames = frames;

    return frames;
};

DYText.prototype.createGif = function (cb) {

    var gif = new GIF({
        workers: 20,
        quality: 10,
        workerScript: './js/gif.worker.js'
    });


    var frames = this.createFrames();

    for (let index = 0; index < frames.length; index++) {

        gif.addFrame(frames[index], {
            delay: 200
        })

    };


    gif.on('finished', function (blob) {

        // cb(URL.createObjectURL(blob));
        //blob 2 base64
        var blobToBase64 = function (blob, callback) {
            var a = new FileReader();
            a.onload = function (e) {
                callback(e.target.result);
            }
            a.readAsDataURL(blob);
        };

        blobToBase64(blob, function (data) {
            cb(data);
        });
    });

    gif.render();
};